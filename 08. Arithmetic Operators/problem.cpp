#include <iostream>
using namespace std;

int main()
{
    short int a = 10, b = 3;

    cout << "Please enter the first number a: \n";
    cin >> a;
    cout << "Please enter the first number b: \n";
    cin >> b;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;


    return 0;
}